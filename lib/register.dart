addSubCollection(String idparam, String foodName, String imageUrl) async {
  FirebaseFirestore firebaseFirestore = FirebaseFirestore.instance;
  var user = FirebaseAuth.instance.currentUser;
  await FirebaseFirestore.instance
      .collection('users')
      .doc(user!.uid)
      .collection("UserFav")
      .add({'id': idparam, 'foodName': foodName, 'imageUrl': imageUrl});
}

getSubCollection(String id) {
  FirebaseFirestore firebaseFirestore = FirebaseFirestore.instance;
  var user = FirebaseAuth.instance.currentUser;
  return FirebaseFirestore.instance
      .collection('users')
      .doc(user!.uid)
      .collection("UserFav")
      .doc(id)
      .get();
}

String returnUserId() {
  var user = FirebaseAuth.instance.currentUser;
  return user!.uid as String;
}
